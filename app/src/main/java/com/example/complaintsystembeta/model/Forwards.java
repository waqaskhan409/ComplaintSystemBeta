package com.example.complaintsystembeta.model;

public class Forwards {
    private String forward;
    private String forward1;

    public Forwards(String forward, String forward1) {
        this.forward = forward;
        this.forward1 = forward1;
    }

    public String getForward() {
        return forward;
    }

    public void setForward(String forward) {
        this.forward = forward;
    }

    public String getForward1() {
        return forward1;
    }

    public void setForward1(String forward1) {
        this.forward1 = forward1;
    }
}
